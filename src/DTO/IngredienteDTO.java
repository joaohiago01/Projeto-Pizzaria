package DTO;

public class IngredienteDTO {

	private String nome;
	private double preco;
	private String[] tableIngrediente;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public double getPreco() {
		return preco;
	}
	public void setPreco(double preco) {
		this.preco = preco;
	}
	public String[] getTableIngrediente() {
		return tableIngrediente;
	}
	public void setTableIngrediente(String[] tableIngrediente2) {
		this.tableIngrediente = tableIngrediente2;
	}
	
	
}
